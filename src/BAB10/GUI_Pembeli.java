    public void koneksi() {
        try {
            conn = null;
            Class.forName("com.mysql.cj.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost/db_sapi?user=root&password=");
        } catch (SQLException e) {
            Logger.getLogger(GUI_DataPembeli.class.getName()).log(Level.SEVERE, null, e);
        } catch (Exception es) {
            Logger.getLogger(GUI_DataPembeli.class.getName()).log(Level.SEVERE, null, es);
        }
    }

    public void tampil() {
        DefaultTableModel tabelhead = new DefaultTableModel();
        tabelhead.addColumn("ID");
        tabelhead.addColumn("NAME");
        tabelhead.addColumn("ADDRESS");
        tabelhead.addColumn("PHONE");
        try {
            String sql = "SELECT * FROM tb_pembeli";
            Statement stat = conn.createStatement();
            ResultSet res = stat.executeQuery(sql);
            while (res.next()) {
                tabelhead.addRow(new Object[]{res.getString(1), res.getString(2), res.getString(3), res.getString(4)});
            }
            tPenyewa.setModel(tabelhead);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "BELUM TERKONEKSI");
        }
    }

    public void insert() {
        try {
            ID = txtID.getText();
            Nama = txtNama.getText();
            Alamat = txtAlamat.getText();
            Telp = txtTelp.getText();
            Statement statement = conn.createStatement();
            statement.executeUpdate("INSERT INTO tb_pembeli VALUES('" + ID + "','" + Nama + "','" + Alamat + "','" + Telp + "')");
            statement.close();
            JOptionPane.showMessageDialog(null, "Berhasil Memasukan Data !" + "\n");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error : " + e);
        }
        refresh();
    }

    public void update() {
        ID = txtID.getText();
        Nama = txtNama.getText();
        Alamat = txtAlamat.getText();
        Telp = txtTelp.getText();
        try {
            Statement statement = conn.createStatement();
            statement.executeUpdate("UPDATE tb_pembeli SET ID='" + ID + "'," + "Name='" + Nama + "',"
                    + "Address='" + Alamat + "'" + ",Phone='" + Telp + "' WHERE ID_Customer = '" + LastID + "'");
            statement.close();
            conn.close();
            JOptionPane.showMessageDialog(null, "Update Data Berhasil!");
        } catch (Exception e) {
            System.out.println("Error : " + e);
        }
        refresh();
    }

    public void delete() {
        int ok = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin akan menghapus data ?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (ok == 0) {
            try {
                String sql = "DELETE FROM tb_pembeli WHERE ID='" + LastID + "'";
                java.sql.PreparedStatement stmt = conn.prepareStatement(sql);
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data  Berhasil di hapus");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error : " + e);
            }
        }
        refresh();
    }

    public void cari() {
        try {
            try (Statement statement = conn.createStatement()) {
                String sql = "SELECT * FROM tb_pembeli WHERE `ID`  LIKE '%" + txtCari.getText() + "%'";
                ResultSet rs = statement.executeQuery(sql); //menampilkan data dari sql query
                if (rs.next()) // .next() = scanner method
                {
                    txtID.setText(rs.getString(1));
                    txtNama.setText(rs.getString(2));
                    txtAlamat.setText(rs.getString(3));
                    txtTelp.setText(rs.getString(4));
                } else {
                    JOptionPane.showMessageDialog(null, "Data yang Anda cari tidak ada");
                }
            }
        } catch (Exception ex) {
            System.out.println("Error." + ex);
        }
    }

